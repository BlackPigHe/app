package com.web.master.component.test.common;

public class UUIDBean {
	private boolean date;
	private String stable;
	private int flowLength;
	private int lastFlow;
	private String split;
	private String updateDate;

	public boolean isDate() {
		return date;
	}

	public void setDate(boolean date) {
		this.date = date;
	}

	public String getStable() {
		return stable;
	}

	public void setStable(String stable) {
		this.stable = stable;
	}

	public int getFlowLength() {
		return flowLength;
	}

	public void setFlowLength(int flowLength) {
		this.flowLength = flowLength;
	}

	public int getLastFlow() {
		return lastFlow;
	}

	public void setLastFlow(int lastFlow) {
		this.lastFlow = lastFlow;
	}

	public String getSplit() {
		return split;
	}

	public void setSplit(String split) {
		this.split = split;
	}

	public String getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}
	
}
